/**
 * Represents different materials used for motorcycle frames.
 * Defines common frame materials with varying properties.
 */
enum Material {
    /**
     * Steel frame material - strong and durable
     */
    STEEL,

    /**
     * Aluminum frame material - lightweight and corrosion-resistant
     */
    ALUMINUM,

    /**
     * Carbon fiber frame material - very lightweight and strong
     */
    CARBON_FIBER,

    /**
     * Titanium frame material - strong, lightweight, and expensive
     */
    TITANIUM
}